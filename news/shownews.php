						<div><p style="text-indent: 2em;">“当前，加强中小学、幼儿园家庭教育工作的指导非常迫切而且意义重大。”2015年底，国家教育部印发了《关于加强家庭教育工作的指导意见》一文，当时教育部相关负责人在答记者问时便如此提到。就在本月又有相关文件出台，全国妇联联合教育部、民政部、文化部等9部门共同印发了《关于指导推进家庭教育的五年规划（2016-
 2020年）》（以下简称《规划》）。</p><p style="text-indent: 2em;">家庭教育被放到了前所未有的高度，随着一系列相关文件的出台，家庭教育也就此有了“国家标准”。必须意识到的是，相关文件出台的背后，其实是家庭教育本身的不足和缺失，这一缺失，不仅在家长本身，同时还有学校、政府、社会整体的责任。</p><p style="text-align: center;"><img data-original="http://10.10.10.183/M1996003/368/upload/201611/1479710697132847.jpg" title="1479710697132847.jpg" alt="2.7.jpg"/></p><p style="text-indent: 2em;">培训班大热是家庭教育缺失的体现</p><p style="text-indent: 2em;">幼儿、少儿培训热火朝天，正是家庭教育缺失的一个体现。乐高课程、英语培训、奥数学习、舞蹈等特长培训，家长们将孩子送往一个个的培训班，期望“不要输在起跑线上”。不过，这看似努力的背后，却正是因为家庭教育自身的不足。</p><p style="text-indent: 2em;">此前年中发布的《2016年中国家庭教育焦点问题调查报告》（以下简称“《报告》”）显示，当前家庭教育存在不少问题，低龄儿童智力过早开发、孩子素质能力缺失尤为突出。家庭教育不足的情况下，却又陷入另外一个怪圈———过高的期望和焦虑齐飞。</p><p style="text-indent: 2em;">家庭教育的责任，并不单只在家庭之上。按照《规定》所言，还需要建立完善的公共服务网络，以及建立社区教育机构、儿童之家、青少年宫、儿童活动中心等活动阵地。</p><p style="text-indent: 2em;">和学校教育的更好结合，是目前关注的另一重点。目前来看学校教育如何与家庭教育结合起来，光有家委会就够了吗？明显不是。相比学校，孩子呆在家庭、与父母相处的时间实际上更长。以幼儿园为例，一年365天当中，除去寒暑假等节假日，在园时间事实上不足200天，每天在园时间也不足10小时。剩下的更多时间和场景，都在家庭。然而，不少家长却怀着这样的念头：孩子上学去了，成绩高低、品行好坏，学校要负最大责任。</p><div id="metinfo_additional"></div></div>
